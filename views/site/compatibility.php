      <h3>浏览器兼容性</h3>
    </div>
    <p>web 技术的发展是迅猛的，这也是这个领域激动人心之所在。然而使用新技术最大的问题是，要为向后兼容付出巨大的代价，甚至根本无法向后兼容。所以我们必须作出作出一些取舍，为此，我们不再对所有浏览器提供一致的支持，而是分级兼容。</p>

    <br>
    <h4>完全兼容的浏览器</h4>
    <p>现代浏览器，是我们开发时使用的浏览器，使用这些浏览器你可以得到最好的体验。这些浏览器包括：</p>
    <ul>
        <li>
            <a href="http://www.google.cn/intl/zh-CN/chrome/" title="谷歌浏览器">Google Chrome</a>
        </li>
        <li>
            <a href="http://firefox.com.cn/" title="火狐浏览器">Mozilla Firefox</a>
        </li>
        <li>
            <a href="http://www.opera.com/zh-cn">Opera</a>
        </li>
        <li>
            <a href="http://www.apple.com.cn/safari/">Safari</a>
        </li>
        <li>IE9 及以上版本</li>
        <li>绝大多数智能移动设备（智能手机、平板）的浏览器</li>
    </ul>

    <br>
    <h4>尽量兼容的浏览器</h4>
    <p>这类浏览器不是现代浏览器，但也不算落后，由于使用人数众多，我们会尽量兼容，保证正常浏览，但不保证提供最好的支持。其中包括：IE8、Android 2.x 浏览器。</p>

    <br>
    <h4>不再兼容的浏览器</h4>
    <p>对于已经过时或功能有限的浏览器，我们不再提供支持，这包括：IE8 以下版本浏览器、非智能手机浏览器。</p>

    <br>
    <h4>其他浏览器</h4>
    <p>国产浏览器并非纯粹的IE或Chromium，但是基于这两者。如果你的页面看起来很糟糕，很可能是使用的低版本IE内核；如果你使用的是双内核浏览器，请使用极速（高速）模式。</p>
